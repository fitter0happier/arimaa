package com.cvut.fel.pjv.Utilities;

/**
 * The Figures enum represents the different types of game pieces.
 * It defines six constants, each corresponding to a specific type of figure.
 */
public enum Figures {
    /**
     * Represents the rabbit piece in the game.
     */
    RABBIT,

    /**
     * Represents the cat piece in the game.
     */
    CAT,

    /**
     * Represents the dog piece in the game.
     */
    DOG,

    /**
     * Represents the horse piece in the game.
     */
    HORSE,

    /**
     * Represents the camel piece in the game.
     */
    CAMEL,

    /**
     * Represents the elephant piece in the game.
     */
    ELEPHANT
}
